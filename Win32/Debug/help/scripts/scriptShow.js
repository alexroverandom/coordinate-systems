function isTrue(x) {
    var plus = (x - x % 100) / 10;
    RESULT = RESULT + plus;
    refresh();
    document.getElementById(x).style.visibility = 'hidden';
    var y = parseInt(x) + 1;
    document.getElementById(y.toString()).style.visibility = 'hidden';
    var z = y + 1;
    document.getElementById(z.toString()).style.visibility = 'hidden';
}

